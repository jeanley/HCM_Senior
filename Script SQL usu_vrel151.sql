SELECT	R046VER.NUMEMP,
		R034FUN.CODFIL,
		R030FIL.NOMFIL,
        R046VER.CODEVE,
		R008EVC.DESEVE,
		R046VER.CODCAL,
		SUM(R046VER.VALEVE) AS VALEVE
FROM	R046VER,
		R034FUN,
		R030FIL,
		R008EVC,
		R044CAL
WHERE	(R046VER.NUMEMP = R034FUN.NUMEMP AND R046VER.TIPCOL = R034FUN.TIPCOL AND R046VER.NUMCAD = R034FUN.NUMCAD)
AND     (R034FUN.NUMEMP = R030FIL.NUMEMP AND R034FUN.CODFIL = R030FIL.CODFIL)
AND		(R046VER.TABEVE = R008EVC.CODTAB AND R046VER.CODEVE = R008EVC.CODEVE)
AND	    (R046VER.CODCAL = R044CAL.CODCAL AND R046VER.NUMEMP = R044CAL.NUMEMP AND R044CAL.INICMP > GETDATE() - 180)
AND     (R046VER.NUMEMP = 2)
AND     (R046VER.CODCAL = 21329)
AND		(R046VER.CODEVE IN (2302,2304) )
GROUP   BY R046VER.NUMEMP,
        R046VER.CODEVE,
		R034FUN.CODFIL,
		R030FIL.NOMFIL,
		R008EVC.DESEVE,
		R046VER.CODCAL
ORDER   BY R034FUN.CODFIL
